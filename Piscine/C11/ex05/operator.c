int	plus(int num1, int num2)
{
	return (num1 + num2);
}

int	minus(int num1, int num2)
{
	return (num1 - num2);
}

int	multiply(int num1, int num2)
{
	return (num1 * num2);
}

int	division(int num1, int num2)
{
	return (num1 / num2);
}

int	modulo(int num1, int num2)
{
	return (num1 % num2);
}
