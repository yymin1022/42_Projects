void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (*(s + i) && i < n)
	{
		if (*(s + i) == (unsigned char)c)
			return (s + i);
		i++;
	}
	return (0);
}
