int	ft_strcmp(char *s1, char *s2)
{
	int	idx;

	idx = 0;
	while (*(s1 + idx) && *(s2 + idx))
	{
		if (*(s1 + idx) > *(s2 + idx))
			return (1);
		else if (*(s1 + idx) < *(s2 + idx))
			return (-1);
	   idx++;	
	}
	return (0);
}
