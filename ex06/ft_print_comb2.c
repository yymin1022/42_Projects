#include <unistd.h>

void	ft_print_comb2(void)
{
	char	c[4];

	c[0] = '0';
	while (c[0] <= '9')
	{
		c[1] = '0';
		while (c[1] <= '9')
		{
			c[2] = '0';
			while (c[2] <= '9')
			{
				c[3] = c[1] + 1;
				if (c[1] == 9)
				{
					c[2] = c[0] + 1;
					c[3] = '0';
				}
				while (c[3] <= '9')
				{
					write(1, c, 1);
					write(1, c + 1, 1);
					write(1, " ", 1);
					write(1, c + 2, 1);
					write(1, c + 3, 1);
					write(1, ", ", 2);
					c[3]++;
				}
				c[2]++;
			}
			c[1]++;
		}
		c[0]++;
	}
}

int main(){ft_print_comb2();}
