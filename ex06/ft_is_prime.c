#include <stdio.h>

int	ft_is_prime(int nb)
{
	long long	x;

	if (nb < 2)
		return (0);
	x = 2;
	while (x * x <= nb)
	{
		if (nb % x == 0)
			return (0);
	}
	return (1);
}
