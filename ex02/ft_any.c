int	ft_any(char **tab, int (*f)(char*))
{
	int	idx;

	idx = 0;
	while (*(tab + idx) != 0)
	{
		if (f(*(tab + idx)) != 0)
			return (1);
		idx++;
	}
	return (0);
}
